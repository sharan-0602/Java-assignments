package week1.day2;

public class IsPrimeNumber {
public static void main(String[] args) {
	for (int i = 2; i <=10; i++) {
		
		if (i%2==0) {
			System.out.println("The given number"+i+"is nonprime number");
		}
			else{
				System.out.println("The given number"+i+"is prime number");
			};
		
		
	}
}
}
